document.addEventListener('DOMContentLoaded', function() {
    const slider = document.getElementById('mySlider');

    const maximum_max = 19.756;
    const maximum_min = 16.801;
    const minimun_max = 3.895;
    const minimun_min = 1.913;
    const one_max = (maximum_max - maximum_min) / 100
    const one_min = (minimun_max - minimun_min) / 100

    const value_chart_max = document.querySelector('.value_chart6_max')
    const value_chart_min = document.querySelector('.value_chart6_min')
    const value_box = document.querySelector('.value-box6')
    const value_number_max = document.querySelector('.value_number6_max')
    const value_number_min = document.querySelector('.value_number6_min')
    slider.addEventListener('input', function() {
        sliderValue = slider.value;

        if(sliderValue >= 1979 && sliderValue <= 2023){
            value_box.style.opacity = 1;

            var releventData = AntarticaSeaLevel.filter(data => (data.year) == sliderValue)
            var value_max = releventData[0].max;
            // value = Math.floor(value * 100) / 100;
            value_number_max.innerText = value_max
            const percent_max = parseInt((value_max - maximum_min)/one_max)
            value_chart_max.style.width =percent_max + "%";

            var value_min = releventData[0].min;
            // value = Math.floor(value * 100) / 100;
            value_number_min.innerText = value_min;
            const percent_min = parseInt((value_min - minimun_min)/one_min)
            value_chart_min.style.width =percent_min + "%";

        }else if(sliderValue < 1979){
            value_box.style.opacity = 0.5;
            value_chart_max.style.width =1 + "%";
            value_number_max.innerText = maximum_min
            value_chart_min.style.width =1 + "%";
            value_number_min.innerText = minimun_min
        }
    });
});

